    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String fname = fnameTxt.getText();
        String lname = lnameTxt.getText();
        String address = addressTxt.getText();
        String contact = contactTxt.getText();
        String email = emailTxt.getText();
        
        String SQL = "insert into myaddress values (?,?,?,?,?)";
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(this,"Driver Not Found");
        }
      
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/addressbook", "root", "7278");
            PreparedStatement st = conn.prepareStatement(SQL);
            st.setObject(1, fname);
            st.setObject(2, lname);
            st.setObject(3, address);
            st.setObject(4, contact);  
            st.setObject(5, email);
            int res = st.executeUpdate();
            if (res>0) {
                 JOptionPane.showMessageDialog(this,"Contact Added !");
                 fnameTxt.setText(null);
                 lnameTxt.setText(null);
                 addressTxt.setText(null);
                 contactTxt.setText(null);
                 emailTxt.setText(null);
            }else{
                 JOptionPane.showMessageDialog(this,"Try Again !");
            } 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

