    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String SQL = "select * from myaddress where firstname = ? ";
        
        String fname = searchTxt.getText();
       
        try {
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(this,"Driver Not Found");
        }
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/addressbook", "root", "7278");
            PreparedStatement st = conn.prepareStatement(SQL);
            st.setObject(1, fname);
            
            ResultSet rst = st.executeQuery();
            
            if (rst.next()) {
                fnameTxt.setText(rst.getString("firstname"));
                lnameTxt.setText(rst.getString("lastname"));
                addressTxt.setText(rst.getString("address"));
                contactTxt.setText(rst.getString("contact"));
                emailTxt.setText(rst.getString("email"));
            
            }else{
                 JOptionPane.showMessageDialog(this,"Contact Not Founde !");
            } 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSearchActionPerformed

